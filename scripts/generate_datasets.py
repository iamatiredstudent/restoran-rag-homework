def makerecord(i):
    restaurant = restauranttypes[i % len(restauranttypes)]
    topic = topics[i % len(topics)]
    ingredient = ingredients[i % len(ingredients)]
    problem = problems[i % len(problems)]
    recommendation = recommendations[i % len(recommendations)]
    metric = metrics[i % len(metrics)]

    month = (i % 12) + 1
    demandgrowth = 5 + (i % 35)
    writeoffshare = 2 + (i % 18)
    stockdays = 3 + (i % 20)

    name = f"Кейс {i:04d}: {topic} — {restaurant}"

    text = (
        f"В {restaurant} рассматривается задача: {topic}. "
        f"Основной продуктовый блок: {ingredient}. "
        f"Проблема заключается в том, что {problem}. "
        f"В месяце {month:02d} наблюдалось изменение спроса на {demandgrowth} процентов, "
        f"а доля списаний по выбранной категории составила {writeoffshare} процентов. "
        f"Средний запас по категории поддерживался на уровне {stockdays} дней продаж. "
        f"Для управления ситуацией рекомендуется {recommendation}. "
        f"Ключевая метрика для контроля результата: {metric}. "
        f"Если значение метрики ухудшается, ресторану следует проверить данные о продажах, "
        f"остатках, поставках, списаниях и технологических картах. "
        f"Документ может использоваться для ответа на вопросы о запасах, закупках, прогнозировании спроса, "
        f"списаниях, поставщиках, маржинальности и контроле качества данных."
    )

    return {
        "id": i,
        "name": name,
        "text": text,
    }


def main():
    if not outpath.parent.exists():
        outpath.parent.mkdir(parents=True)

    records = []

    for i in range(1, 1201):
        records.append(makerecord(i))

    data = {
        "datasets": records
    }

    text = json.dumps(data, indent=2)
    outpath.write_text(text, encoding="utf-8")

    print(f"Created {outpath}")
    print(f"Records: {len(records)}")


main()
